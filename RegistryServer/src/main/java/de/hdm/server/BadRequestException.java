package de.hdm.server;

import de.hdm.exceptions.RegistryServerException;

/**
 * This exception is thrown, if an error in relation to a request occurs. For example is a required query parameter is
 * missing or it's value is invalid.
 * 
 * This class is not thread safe.
 * 
 * @author Tobias Ableitner
 *
 */
public class BadRequestException extends RegistryServerException {

	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// attributes
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************

	/**
	 * Generated serial version UID.
	 */
	private static final long serialVersionUID = -4279972971394748587L;




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// constructors
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************

	/**
	 * Creates a new {@link BadRequestException} without a user message and an original exception.
	 * 
	 * @param message
	 *            the error message.
	 */
	public BadRequestException(String message) {
		this(message, null);
	}

	/**
	 * Creates a new {@link BadRequestException} without a user message but an original exception. The original
	 * exception is an exception, which raises this exception and was caught before throwing this exception.
	 * 
	 * @param message
	 *            the error message.
	 * @param originalException
	 *            the exception which raised this error message
	 */
	public BadRequestException(String message, Exception originalException) {
		this(message, originalException, null);
	}

	/**
	 * Creates a new {@link BadRequestException} with user message and original exception. The user message is an error
	 * message which can be shown to the user. The original exception is an exception, which raises this exception and
	 * was caught before throwing this exception.
	 * 
	 * @param message
	 *            the error message
	 * @param originalException
	 *            the exception which raised this error message
	 * @param userMessage
	 *            an error message which can be shown to the user
	 */
	public BadRequestException(String message, Exception originalException, String userMessage) {
		super(message, originalException, userMessage);
	}




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// getters and setters
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// abstract methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// override methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// public methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// protected methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// private methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// inner classes
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************

}
