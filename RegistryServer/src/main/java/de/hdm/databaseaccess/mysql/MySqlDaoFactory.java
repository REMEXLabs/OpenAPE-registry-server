package de.hdm.databaseaccess.mysql;

import de.hdm.databaseaccess.DaoFactory;
import de.hdm.databaseaccess.IConceptDao;
import de.hdm.databaseaccess.IGeneratedConceptIdDao;
import de.hdm.databaseaccess.IGroupAccessRightDao;
import de.hdm.databaseaccess.IGroupDao;
import de.hdm.databaseaccess.ILanguageDao;
import de.hdm.databaseaccess.ILogDao;
import de.hdm.databaseaccess.IOwnershipDao;
import de.hdm.databaseaccess.IUserDao;

/**
 * This class is part of a factory design pattern. It extends the class {@link DaoFactory} by implementing the create
 * data access object methods for the MySQL database.
 * 
 * This class is thread safe.
 * 
 * @author Tobias Ableitner
 *
 */
public class MySqlDaoFactory extends DaoFactory {

	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// attributes
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// constructors
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************

	/**
	 * Empty default constructor.
	 */
	public MySqlDaoFactory() {

	}




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// getters and setters
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// abstract methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// override methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************

	/*
	 * (non-Javadoc)
	 * 
	 * @see de.hdm.databaseaccess.DaoFactory#createLanguageDao()
	 */
	@Override
	public ILanguageDao createLanguageDao() {
		return new MySqlLanguageDao();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see de.hdm.databaseaccess.DaoFactory#createConceptDao()
	 */
	@Override
	public IConceptDao createConceptDao() {
		return new MySqlConceptDao();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see de.hdm.databaseaccess.DaoFactory#createUserDao()
	 */
	@Override
	public IUserDao createUserDao() {
		return new MySqlUserDao();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see de.hdm.databaseaccess.DaoFactory#createOwnershipDao()
	 */
	@Override
	public IOwnershipDao createOwnershipDao() {
		return new MySqlOwnershipDao();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see de.hdm.databaseaccess.DaoFactory#createGroupAccessRightDao()
	 */
	@Override
	public IGroupAccessRightDao createGroupAccessRightDao() {
		return new MySqlGroupAccessRightDao();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see de.hdm.databaseaccess.DaoFactory#createLogDao()
	 */
	@Override
	public ILogDao createLogDao() {
		return new MySqlLogDao();
	}

	/*
	 * (non-Javadoc)
	 * 
	 * @see de.hdm.databaseaccess.DaoFactory#createGeneratedConceptIdDao()
	 */
	@Override
	public IGeneratedConceptIdDao createGeneratedConceptIdDao() {
		return new MySqlGeneratedConceptIdDao();
	}
	
	/*
	 * (non-Javadoc)
	 * 
	 * @see de.hdm.databaseaccess.DaoFactory#createGroupDao()
	 */
	@Override
	public IGroupDao createGroupDao() {
		return new MySqlGroupDao();
	}




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// public methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// protected methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// private methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// inner classes
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************

}
