package de.hdm.datatypes;

/**
 * This class is used to define a name of a concept.
 * 
 * This class is thread safe.
 * 
 * @author Tobias Ableitner
 *
 */
public class Name extends Content implements IName {

	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// attributes
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// constructors
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************

	/**
	 * Creates a new Name and set's the value of {@link Content#id} to -1. This constructor should be used, to
	 * instantiate names, which are not already stored in the database.
	 * 
	 * @param languageCode
	 *            the language code of the name's language. If the name is not language specific, it can be null. But if
	 *            it is empty an {@link IllegalArgumentException} will be thrown.
	 * @param conceptId
	 *            the concept's id to which the name belongs. It must not be null or empty. Otherwise an
	 *            {@link IllegalArgumentException} will be thrown.
	 * @param name
	 *            the name it self or in other words, the name's value. It can be empty. But if it null, an
	 *            {@link IllegalArgumentException} will be thrown.
	 */
	public Name(String languageCode, String conceptId, String content) {
		super(languageCode, conceptId, content);
	}

	/**
	 * Creates a new Name and set's the value of {@link Content#id} to -1. This constructor should be used, to
	 * instantiate names, which are already stored in the database.
	 * 
	 * @param id
	 *            the id of the name in the database. It must be greater equals 1. Otherwise an
	 *            {@link IllegalArgumentException} will be thrown.
	 * @param languageCode
	 *            the language code of the name's language. If the name is not language specific, it can be null. But if
	 *            it is empty an {@link IllegalArgumentException} will be thrown.
	 * @param conceptId
	 *            the concept's id to which the name belongs. It must not be null or empty. Otherwise an
	 *            {@link IllegalArgumentException} will be thrown.
	 * @param name
	 *            the name it self or in other words, the name's value. It can be empty. But if it null, an
	 *            {@link IllegalArgumentException} will be thrown.
	 */
	public Name(int id, String languageCode, String conceptId, String content) {
		super(id, languageCode, conceptId, content);
	}




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// getters and setters
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// abstract methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// override methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// public methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// protected methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// private methods
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************




	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************
	// inner classes
	// *********************************************************************************************************************************************
	// *********************************************************************************************************************************************

}
